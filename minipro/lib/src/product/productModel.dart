class ModelProduct {
  final String image;
  final String title;
  final String price;
  final int area;
  final int size;
  final int bedroom;
  final int bathroom;
  final String location;
  final int floor;

  ModelProduct({
    required this.image,
    required this.title,
    required this.price,
    required this.area,
    required this.size,
    required this.bedroom,
    required this.bathroom,
    required this.location,
    required this.floor,
  });
}
